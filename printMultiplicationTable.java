/*Print Multiplication Table
Problem Statement: Write a program to print the multiplication table of a given number up to 10.
Sample Input: 5
Sample Output: 5 * 1 = 5, 5 * 2 = 10, ..., 5 * 10 = 50
Explanation: The program uses a loop to multiply the input number by 1 to 10, introducing formatted output. */
public class printMultiplicationTable {
    public static void main(String[] args) {
        for(int i=1; i<=10; i++){
            System.out.println(5);
        }
    }
    
}
